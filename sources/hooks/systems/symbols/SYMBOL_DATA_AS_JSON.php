            'SITE_NAME'          => ecv2_SITE_NAME(null, [], []),
            'COPYRIGHT'          => ecv2_COPYRIGHT(null, [], []),
            'DOMAIN'             => ecv2_DOMAIN(null, [], []),
            'FORUM_BASE_URL'     => ecv2_FORUM_BASE_URL(null, [], []),
            'BASE_URL'           => ecv2_BASE_URL(null, [], []),
